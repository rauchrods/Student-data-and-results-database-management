    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
                  String rollno = jTextField8.getText();
                  String physicsmarks = jTextField13.getText();
                  String chemistrymarks = jTextField9.getText();
                   String mathematicsmarks = jTextField10.getText();
                  String dbmsmarks = jTextField11.getText();
                  String osmarks = jTextField12.getText();
       
        
         try{
             Class.forName("com.mysql.jdbc.Driver");
             Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "rictreroyrau962415");
             Statement st = con.createStatement();
             ResultSet rs = st.executeQuery("select * from student where rollno='"+rollno+"'");
             if(rs.next()){
                 st.executeUpdate("insert into result(rollno,physicsmarks,chemistrymarks,mathematicsmarks,dbmsmarks,osmarks) values('"+rollno+"','"+physicsmarks+"','"+chemistrymarks+"','"+mathematicsmarks+"','"+dbmsmarks+"','"+osmarks+"')");
             JOptionPane.showMessageDialog(null,"Succesfully added a result ");
             setVisible(false);
             new insertnewResult().setVisible(true);
             }
             else{
                 JOptionPane.showMessageDialog(null,"Roll no doesnot exist in the Student table ");
             }
             
         }
         catch(Exception e){
             JOptionPane.showMessageDialog(null,e.toString());
         }            

    }//GEN-LAST:event_jButton5ActionPerformed

